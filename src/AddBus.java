    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String ownerName = owner.getText();
        String busName = bus.getText();
        String fromPlace = from.getText();
        String toPlace = to.getText();
        String timeReg = time.getText();
        
        try {
            st = con.prepareStatement("INSERT INTO owners(Name,BusNm,startPlace,destination,Time) values (?,?,?,?,?)");
            st.setString(1,ownerName);
            st.setString(2,busName);
            st.setString(3,fromPlace);
            st.setString(4,toPlace);
            st.setString(5,timeReg);
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Bus Details added");
            owner.setText("");
            bus.setText("");
            from.setText("");
            to.setText("");
            time.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(AddBus.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Check the details entered");
        }

        
    }//GEN-LAST:event_jButton2ActionPerformed

